export default class PopupView {
  constructor({movie}) {
    this.movie = movie;
}

  getTemplate() {
    return createPopupTemplate(this.movie);
  }

  getElement() {
    if (!this.element) {
      this.element = createElement(this.getTemplate());
    }

    return this.element;
  }

  removeElement() {
    this.element = null;
  }
}
